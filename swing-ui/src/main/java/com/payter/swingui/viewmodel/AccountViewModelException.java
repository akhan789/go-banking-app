public class AccountViewModelException extends Exception {

    private static final long serialVersionUID = 9015841574636082107L;

    public AccountViewModelException() {
        super();
    }

    public AccountViewModelException(String message) {
        super(message);
    }

    public AccountViewModelException(String message, Throwable cause) {
        super(message, cause);
    }

    public AccountViewModelException(Throwable cause) {
        super(cause);
    }
}